  FRONTEND_HOST="$FRONTEND_HOST" \
  FRONTEND_PORT="$FRONTEND_PORT" \
  FRONTEND_LISTENER_HOST="$FRONTEND_LISTENER_HOST" \
  FRONTEND_LISTENER_PORT="$FRONTEND_LISTENER_PORT" \
  FRONTEND_LISTENER_AUTH_TOKEN="$FRONTEND_LISTENER_AUTH_TOKEN" \
  FRONTEND_BOOTSTRAP_ADMIN_USERNAME="$FRONTEND_BOOTSTRAP_ADMIN_USERNAME" \
  FRONTEND_BOOTSTRAP_ADMIN_PASSWORD="$FRONTEND_BOOTSTRAP_ADMIN_PASSWORD" \
  FRONTEND_SESSION_TTL_SECONDS="$FRONTEND_SESSION_TTL_SECONDS" \
  FRONTEND_SESSION_SECURE_COOKIE="$FRONTEND_SESSION_SECURE_COOKIE" \
  FRONTEND_DB_DSN="$FRONTEND_DB_DSN" \
  DB_DSN="$FRONTEND_DB_DSN" \
  "$PYTHON_BIN" "$SCRIPT_DIR/server.py"
