HOST="${FRONTEND_DSQL_HOST:-${DSQL_HOST:-4btv7qq43k4ztw3tjubmbyf3su.dsql.us-east-2.on.aws}}"
REGION="${FRONTEND_AWS_REGION:-${AWS_REGION:-us-east-2}}"
FRONTEND_HOST="${FRONTEND_HOST:-0.0.0.0}"
FRONTEND_PORT="${FRONTEND_PORT:-8080}"
FRONTEND_LISTENER_HOST="${FRONTEND_LISTENER_HOST:-127.0.0.1}"
